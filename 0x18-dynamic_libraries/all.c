#include <stdio.h>

/**
 * add - this adds two integers
 * @a: this is the first int
 * @b: this is the second int
 * Return: it returns addition
 */

int add(int a, int b)
{
	return(a + b);
}
/**
 * sub - this subss two integers
 * @a: this is the first int
 * @b: this is the second int
 * Return: it returns subtraction
 */

int sub(int a, int b)
{
	return(a - b);
}
/**
 * mul - this mul  two integers
 * @a: this is the first int
 * @b: this is the second int
 * Return: it returns mul
 */

int mul(int a, int b)
{
	return(a * b);
}
/**
 * mod - this mod of two integers
 * @a: this is the first int
 * @b: this is the second int
 * Return: it returns mod
 */

int mod(int a,  int b)
{
	return(a %  b);
}
/**
 * div - this div two integers
 * @a: this is the first int
 * @b: this is the second int
 * Return: it returns division
 */

int my_div(int a, int b)
{
	return(a / b);
}
